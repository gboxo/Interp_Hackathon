import pandas as pd
import numpy as np
from sklearn.metrics import confusion_matrix, classification_report, f1_score, precision_score, recall_score
from tabulate import tabulate

def calculate_metrics(df):
    # Convert 'intent_inspector_judgment' to boolean
    y_true = df['has_attack']
    y_pred = df['intent_inspector_judgment'] == 'SUSPICIOUS'

    # Calculate confusion matrix
    cm = confusion_matrix(y_true, y_pred)

    # Calculate metrics
    f1 = f1_score(y_true, y_pred)
    precision = precision_score(y_true, y_pred)
    recall = recall_score(y_true, y_pred)

    # Create and print confusion matrix
    conf_matrix = pd.DataFrame(cm,
                               index=['Actual Negative', 'Actual Positive'],
                               columns=['Predicted Negative', 'Predicted Positive'])
    print("Confusion Matrix:")
    print(tabulate(conf_matrix, headers='keys', tablefmt='pretty'))
    print()

    # Print metrics
    metrics = {
        'F1 Score': f1,
        'Precision': precision,
        'Recall': recall,
        'True Negatives': cm[0][0],
        'False Positives': cm[0][1],
        'False Negatives': cm[1][0],
        'True Positives': cm[1][1]
    }

    print("Metrics:")
    print(tabulate([(k, v) for k, v in metrics.items()],
                   headers=['Metric', 'Value'],
                   tablefmt='pretty', floatfmt='.4f'))
    print()

    # Print classification report
    print("Classification Report:")
    print(classification_report(y_true, y_pred, target_names=['Not Attack', 'Attack']))

    return metrics

