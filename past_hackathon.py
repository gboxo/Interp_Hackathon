class FlexibleTool:
    """
    Wrapper class for tool functions in the agent system.

    FlexibleTool integrates tool usage with the ToolUsageRecorder,
    allowing for detailed logging of tool invocations across different agents.
    It encapsulates the actual tool function, agent name, and recorder,
    providing a standardized interface for tool execution while ensuring
    all tool uses are properly recorded for analysis and monitoring.
    """

    def __init__(self, agent_name: str, recorder: ToolUsageRecorder, func: Callable, tool_name: str):
        self.agent_name = agent_name
        self.recorder = recorder
        self.func = func
        self.tool_name = tool_name

    def __call__(self, **kwargs) -> str:
        """Record that a tool was called and execute the actual tool function."""
        tool_input = kwargs.get('input', '')
        self.recorder.record('tool_call', self.agent_name, self.tool_name, tool_input)
        return self.func(tool_input)

